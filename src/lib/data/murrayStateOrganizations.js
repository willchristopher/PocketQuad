function normalizeOrganizationName(name) {
  return name?.trim().toLowerCase() ?? '';
}

const MURRAY_STATE_ORGANIZATION_BY_NAME = new Map(
  MURRAY_STATE_ORGANIZATIONS.map((organization) => [normalizeOrganizationName(organization.name), organization]),
);

export function getMurrayStateOrganizationByName(name) {
  return MURRAY_STATE_ORGANIZATION_BY_NAME.get(normalizeOrganizationName(name)) ?? null;
}

export function enrichMurrayStateOrganizationRecord(record) {
  const organization = getMurrayStateOrganizationByName(record?.name);

  if (!organization) {
    return record;
  }

  return {
    ...record,
    category: record.category ?? organization.category ?? null,
    presidentName: record.presidentName ?? organization.presidentName ?? null,
    presidentEmail: record.presidentEmail ?? organization.presidentEmail ?? null,
    advisorName: record.advisorName ?? organization.advisorName ?? null,
    advisorEmail: record.advisorEmail ?? organization.advisorEmail ?? null,
    publicContactInfo: record.publicContactInfo ?? organization.publicContactInfo ?? null,
    sourceUrls: record.sourceUrls ?? organization.sourceUrls ?? null,
    importNotes: record.importNotes ?? organization.importNotes ?? null,
  };
}
